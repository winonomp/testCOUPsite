$(document).ready(function(){
    var boolDrop = true;
    $("#drop1Link").click(function(){
    if(boolDrop === true){
    $("#drop1").css({ display : "none" });
    $("#drop1Icon").css({ "-webkit-transform": "rotate(180deg)",
                            "-moz-transform": "rotate(180deg)",
                             "transform": "rotate(180deg)"});
    $("#drop1Text").text("OPEN DETAILS");
        boolDrop = false;
    }
    else if(boolDrop === false){
        $("#drop1").css({ display : "block" });
        $("#drop1Icon").css({ "-webkit-transform": "rotate(0deg)",
                                "-moz-transform": "rotate(0deg)",
                                "transform": "rotate(0deg)"});
        $("#drop1Text").text("CLOSE DETAILS");
    boolDrop = true;
      }
  });
});

